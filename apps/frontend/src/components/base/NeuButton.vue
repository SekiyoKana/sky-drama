<script setup lang="ts">
import { computed } from 'vue'

const props = withDefaults(defineProps<{
  variant?: 'primary' | 'danger' | 'default'
  disabled?: boolean
  loading?: boolean
  block?: boolean // 是否占满父容器宽度
}>(), {
  variant: 'default',
  disabled: false,
  loading: false,
  block: false
})

const emit = defineEmits(['click'])

// 动态计算样式
const buttonClass = computed(() => {
  const base = [
    'px-6 py-3 rounded-2xl font-bold transition-all duration-200 outline-none',
    'flex items-center justify-center', // 内容居中
    props.block ? 'w-full' : ''
  ]

  // 禁用状态
  if (props.disabled || props.loading) {
    return [...base, 'neu-flat opacity-50 cursor-not-allowed text-gray-400']
  }

  // 正常状态
  const activeEffects = 'neu-flat neu-active hover:-translate-y-0.5' // 悬浮微微上浮，点击凹陷
  
  switch (props.variant) {
    case 'primary':
      return [...base, activeEffects, 'text-blue-600 hover:text-blue-700']
    case 'danger':
      return [...base, activeEffects, 'text-red-500 hover:text-red-600']
    default:
      return [...base, activeEffects, 'text-gray-600 hover:text-gray-800']
  }
})
</script>

<template>
  <button
    :class="buttonClass"
    :disabled="disabled || loading"
    @click="emit('click', $event)"
  >
    <svg v-if="loading" class="w-5 h-5 mr-2 animate-spin" fill="none" viewBox="0 0 24 24">
      <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4"></circle>
      <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
    </svg>
    <slot />
  </button>
</template>